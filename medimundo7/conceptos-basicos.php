                        <li><strong>Buena fe y lealtad:</strong> Actuar con honestidad y no hacer competencia desleal a la empresa.</li>
                        <li><strong>Seguridad:</strong> Es obligatorio usar correctamente los equipos de protección para cuidarnos a nosotras mismas y a las demás.</li>
