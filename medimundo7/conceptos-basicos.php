            <article class="apple-texto">
                <h2 class="apple-texto-titulo">¿Qué es exactamente una relación laboral?</h2>
                <section class="apple-texto-subtitulo">
                    <p>Es el vínculo legal que nace cuando nosotras decidimos, de forma voluntaria, prestar nuestros servicios para otra persona o empresa a cambio de un salario. Para que la ley nos proteja, deben darse cuatro condiciones:</p>
                    <ul>
                        <li><strong>Voluntariedad:</strong> Nadie puede obligarnos a trabajar. Es nuestra decisión firmar y mantener el contrato.</li>
                        <li><strong>Ajenidad:</strong> Los frutos de nuestro trabajo y los riesgos del negocio son de la empresa. Si no hay clientes, nosotras cobramos nuestro salario igualmente.</li>
                        <li><strong>Dependencia:</strong> Trabajamos bajo las normas, horarios y directrices de quien nos contrata.</li>
                        <li><strong>Remuneración:</strong> Siempre debe existir un salario. El trabajo gratuito no es una relación laboral.</li>
                    </ul>
                </section>
