                <h2 class="apple-texto-titulo">Nuestros Derechos Fundamentales</h2>
                <section class="apple-texto-subtitulo">
                    <p>Desde el primer minuto en que empezamos a trabajar, la ley nos otorga un escudo protector. Estos derechos son irrenunciables; ningún contrato puede obligarnos a renunciar a ellos:</p>
                    <ul>
                        <li><strong>Ocupación efectiva:</strong> Tienen que darnos tareas reales y herramientas. No pueden castigarnos dejándonos aisladas sin hacer nada.</li>
                        <li><strong>Igualdad y no discriminación:</strong> Mismo salario y trato, sin importar nuestra edad, origen, orientación sexual o el hecho de ser mujeres.</li>
                        <li><strong>Salud e integridad:</strong> Derecho a un entorno seguro, con descansos diarios y semanales que protejan nuestro cuerpo y mente.</li>
                        <li><strong>Protección contra el acoso:</strong> Tolerancia cero ante actitudes que vulneren nuestra dignidad, ya sea acoso laboral, sexual o por razón de sexo.</li>
                    </ul>
                </section>
